package com.datatrees.common.protocol.http;

import com.datatrees.common.protocol.ProtocolException;

public class HttpException extends ProtocolException {

    private static final long serialVersionUID = 8705032384287879459L;

    public HttpException() {
        super();
    }

    public HttpException(String message) {
        super(message);
    }

    public HttpException(String message, Throwable cause) {
        super(message, cause);
    }

    public HttpException(Throwable cause) {
        super(cause);
    }

}
